# Write a program to perform addition, subtraction, multiplication and division of two numbers.

num1 = int(input("Enter a number: "))
num2 = int(input("Enter a number: "))

sumOf = num1 + num2
subOf = num1 - num2
mulOf = num1 * num2
divOf = num1 / num2

print("The sum of ", num1, 'and', num2, 'is', sumOf)
print("The sub of ", num1, 'and', num2, 'is', subOf)
print("The multiplication of ", num1, 'and', num2, 'is', mulOf)
print("The division of ", num1, 'and', num2, 'is', divOf)
